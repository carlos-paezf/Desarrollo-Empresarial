    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String[] params = new String[4];
        params[Invoice.ID] = txtInvoiceID.getText();
        params[Invoice.INVOICEDATE] = txtInvoiceDate.getText();
        params[Invoice.TOTALPRICE] = txtTotalPrice.getText();
       // params[Invoice.CLIENT] = clientAddressTxfld.getText();

        if (updateFlag) {
            invoiceController.getInvoices().get(tablePos).setId(Integer.parseInt(params[Invoice.ID]));
            refreshInvoiceTable();
        } else {

            if (invoiceController.create(params)) {
                refreshInvoiceTable();
                infoMassageLbl.setText("Guardado Correctamente");
            } else {
                infoMassageLbl.setText("Ah ocurrido un error");
            }
        }
        clearForm();
    }//GEN-LAST:event_saveBtnActionPerformed

    private void listInvoiceTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listInvoiceTableMouseClicked
        listInvoiceTable.getValueAt(listInvoiceTable.getSelectedRow(), listInvoiceTable.getSelectedColumn());
        txtInvoiceID.setText(String.valueOf(invoiceController.getInvoices().get(listInvoiceTable.getSelectedRow()).getId()));
        txtInvoiceDate.setText(invoiceController.getInvoices().get(listInvoiceTable.getSelectedRow()).getInvoiceDate());
        txtTotalPrice.setText(String.valueOf(invoiceController.getInvoices().get(listInvoiceTable.getSelectedRow()).getTotalPrice()));
      //  clientAddressTxfld.setText(invoiceController.getClients().get(listInvoiceTable.getSelectedRow()).getAddress());
        updateFlag = true;
        tablePos = listInvoiceTable.getSelectedRow();
        saveBtn.setText("Modificar");
        btnDelete.setEnabled(true);
    }//GEN-LAST:event_listInvoiceTableMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (invoiceController.delete(tablePos)) {
            refreshInvoiceTable();
            clearForm();
            infoMassageLbl.setText("Se ha eliminado el usuario correctamente");
        } else {
            infoMassageLbl.setText("Ha ocurrido un error (Error : N14), por favor informa al soporte");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

