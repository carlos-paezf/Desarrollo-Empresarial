    private void clearForm() {
        txtInvoiceID.setText("");
        txtInvoiceDate.setText("");
        txtTotalPrice.setText("");
        saveBtn.setText("Guardar");
        updateFlag = false;
        tablePos = -1;
        btnDelete.setEnabled(false);
    }
    
    private void refreshInvoiceTable() {
        try {
            model.setRowCount(0);
            for (Invoice currentInvoice : invoiceController.getInvoices()) {
                model.addRow(new Object[]{
                    currentInvoice.getId(),
                    currentInvoice.getInvoiceDate(),
                    currentInvoice.getTotalPrice(),
                    currentInvoice.getClient()
                });
            }
        } catch (Exception e) {
            infoMassageLbl.setText("Error: " + e.getMessage());
        }
    }
    
